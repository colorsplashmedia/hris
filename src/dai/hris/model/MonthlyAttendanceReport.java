package dai.hris.model;


public class MonthlyAttendanceReport {
	
	private int numCtr;
	private String empName;
	private String sick;
	private String vacation;
	private String hours;
	private String minutes;
	private String undertime;
	private String dayEquivalent;
	private String remarks;
	private String transDate;
	
	
	public int getNumCtr() {
		return numCtr;
	}
	public void setNumCtr(int numCtr) {
		this.numCtr = numCtr;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getSick() {
		return sick;
	}
	public void setSick(String sick) {
		this.sick = sick;
	}
	public String getVacation() {
		return vacation;
	}
	public void setVacation(String vacation) {
		this.vacation = vacation;
	}
	public String getHours() {
		return hours;
	}
	public void setHours(String hours) {
		this.hours = hours;
	}
	public String getMinutes() {
		return minutes;
	}
	public void setMinutes(String minutes) {
		this.minutes = minutes;
	}
	public String getUndertime() {
		return undertime;
	}
	public void setUndertime(String undertime) {
		this.undertime = undertime;
	}
	public String getDayEquivalent() {
		return dayEquivalent;
	}
	public void setDayEquivalent(String dayEquivalent) {
		this.dayEquivalent = dayEquivalent;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getTransDate() {
		return transDate;
	}
	public void setTransDate(String transDate) {
		this.transDate = transDate;
	}
		
	
}
